"""Create a GitHub repo with benchmark corpus files.

Converts CoSQA corpus snippets into real Python files in a repo structure
that any code context engine can index through its normal pipeline.

Usage:
    python -m benchmarks.create_benchmark_repo --org synthetic-sciences
    python -m benchmarks.create_benchmark_repo --org synthetic-sciences --dataset codesearchnet
"""

from __future__ import annotations

import argparse
import json
import re
import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

DATASETS_DIR = Path(__file__).parent / "datasets"


def _sanitize_filename(content: str, doc_id: str) -> str:
    """Derive a meaningful filename from the snippet content."""
    # Try to extract function/class name
    match = re.match(r"(?:def|class)\s+(\w+)", content.strip())
    if match:
        return match.group(1)
    # Fall back to doc ID
    return f"snippet_{doc_id}"


def build_repo_tree(dataset_name: str, output_dir: Path) -> int:
    """Write corpus snippets as files into output_dir.

    Structure:
        python/
            string_utils/
                snippet_001_process_and_sort.py
            ...

    Returns number of files created.
    """
    ds_path = DATASETS_DIR / f"{dataset_name}_benchmark.json"
    if not ds_path.exists():
        print(f"[ERROR] {ds_path} not found")
        return 0

    with open(ds_path) as f:
        data = json.load(f)

    corpus = data["corpus"]
    count = 0

    for i, doc in enumerate(corpus):
        lang = doc.get("language", "python")
        ext = {"python": "py", "javascript": "js", "java": "java",
               "go": "go", "ruby": "rb", "php": "php"}.get(lang, "txt")

        name = _sanitize_filename(doc["content"], doc["id"])
        # Group into subdirs of ~50 files to avoid huge flat dirs
        group = f"group_{i // 50:02d}"
        file_dir = output_dir / lang / group
        file_dir.mkdir(parents=True, exist_ok=True)

        # Ensure unique filename
        fname = f"{i:04d}_{name}.{ext}"
        file_path = file_dir / fname

        file_path.write_text(doc["content"])
        count += 1

    return count


def create_github_repo(
    org: str,
    dataset_name: str,
    output_dir: Path,
) -> str:
    """Create the GitHub repo and push files.

    Returns the repo URL.
    """
    repo_name = f"benchmark-corpus-{dataset_name}"
    full_name = f"{org}/{repo_name}"

    # Check if repo already exists
    result = subprocess.run(
        ["gh", "repo", "view", full_name, "--json", "url"],
        capture_output=True, text=True,
    )
    if result.returncode == 0:
        url = json.loads(result.stdout)["url"]
        print(f"  Repo already exists: {url}")
        print(f"  Delete it first if you want to recreate: gh repo delete {full_name}")
        return url

    # Create repo on GitHub (no clone — we'll init locally)
    result = subprocess.run(
        ["gh", "repo", "create", full_name,
         "--public",
         "--description", f"Benchmark corpus ({dataset_name}) for code context engine evaluation"],
        capture_output=True, text=True,
    )
    if result.returncode != 0:
        print(f"  [ERROR] Failed to create repo: {result.stderr}")
        sys.exit(1)
    print(f"  Created repo on GitHub")

    # Init a git repo in the output dir with the corpus files
    work_dir = output_dir

    # Write a README
    readme = work_dir / "README.md"
    readme.write_text(
        f"# Benchmark Corpus: {dataset_name}\n\n"
        f"Code snippets from the {dataset_name.upper()} dataset, structured as real files "
        f"for indexing by code context engines.\n\n"
        f"**Purpose**: Fair retrieval evaluation — every engine indexes the same repo "
        f"through its normal pipeline.\n\n"
        f"Generated by `benchmarks/create_benchmark_repo.py` in "
        f"[synsc-context](https://github.com/synthetic-sciences/synsc-context).\n"
    )

    # Git init, add, commit, push
    run = lambda cmd: subprocess.run(cmd, cwd=work_dir, check=True, capture_output=True, text=True)
    run(["git", "init", "-b", "main"])
    run(["git", "add", "."])
    run(["git", "commit", "-m", f"Add {dataset_name} benchmark corpus"])
    run(["git", "remote", "add", "origin", f"git@github.com:{full_name}.git"])
    print(f"  Pushing 500 files...")
    run(["git", "push", "-u", "origin", "main"])

    url = f"https://github.com/{full_name}"
    print(f"  Pushed to {url}")
    return url


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Create a GitHub repo with benchmark corpus for cross-engine evaluation",
    )
    parser.add_argument(
        "--org", required=True,
        help="GitHub org or user to create the repo under",
    )
    parser.add_argument(
        "--dataset", default="cosqa",
        choices=["cosqa", "codesearchnet"],
        help="Which dataset to use (default: cosqa)",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Build the file tree locally without pushing to GitHub",
    )
    args = parser.parse_args()

    print(f"\n=== Creating benchmark repo for {args.dataset} ===")

    with tempfile.TemporaryDirectory() as tmpdir:
        output_dir = Path(tmpdir) / "corpus"
        output_dir.mkdir()

        count = build_repo_tree(args.dataset, output_dir)
        print(f"  Created {count} files")

        if args.dry_run:
            # Copy to a visible location
            dry_dir = Path(f"benchmarks/corpus-preview-{args.dataset}")
            if dry_dir.exists():
                shutil.rmtree(dry_dir)
            shutil.copytree(output_dir, dry_dir)
            print(f"  Dry run — files at {dry_dir}/")
            return 0

        url = create_github_repo(args.org, args.dataset, output_dir)
        print(f"\n  Done! Repo: {url}")
        print(f"  Now index it on each engine to run retrieval benchmarks.")

    return 0


if __name__ == "__main__":
    sys.exit(main())
